public class UnaryOperator {
        public static void main(String [] args){
            System.out.println("Unary Operator");
            int i=5;
            System.out.println(i++);    //here the value of i is 5
            System.out.println(i);  //here the value of i is incremented to 6
            System.out.println(++i);    //here the values previously incremented value i.e., 6 now incremented to7
        }
    }

